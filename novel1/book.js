// ===================================================
// Ambient floating particles (dipakai di kedua halaman)
// ===================================================
function spawnParticles(containerId, count = 28) {
  const container = document.getElementById(containerId);
  if (!container) return;
  for (let i = 0; i < count; i++) {
    const p = document.createElement("span");
    p.className = "particle";
    const size = Math.random() * 3 + 2;
    p.style.width = size + "px";
    p.style.height = size + "px";
    p.style.left = Math.random() * 100 + "%";
    p.style.animationDuration = Math.random() * 10 + 8 + "s";
    p.style.animationDelay = Math.random() * 8 + "s";
    container.appendChild(p);
  }
}

// ===================================================
// Iris scene-transition helper (gaya transisi anime)
// ===================================================
function irisClose(overlay, callback) {
  overlay.classList.add("iris-closing");
  overlay.addEventListener(
    "transitionend",
    function handler() {
      overlay.removeEventListener("transitionend", handler);
      callback();
    },
    { once: true }
  );
}

// ===================================================
// Halaman: index.html
// ===================================================
function initLanding() {
  const startBtn = document.getElementById("startBtn");
  const overlay = document.getElementById("irisOverlay");
  if (!startBtn || !overlay) return;

  startBtn.addEventListener("click", () => {
    irisClose(overlay, () => {
      window.location.href = "read.html";
    });
  });
}

// ===================================================
// Halaman: read.html
// ===================================================
let currentEpisode = 0;

function renderEpisodeList() {
  const list = document.getElementById("episodeList");
  if (!list) return;
  novel.episodes.forEach((ep, i) => {
    const li = document.createElement("li");
    li.className = "episode-item";
    li.dataset.index = i;
    li.tabIndex = 0;
    li.innerHTML =
      '<span class="ep-number">' + String(i + 1).padStart(2, "0") + "</span>" +
      '<span class="ep-title">' + ep.title + "</span>";
    li.addEventListener("click", () => goToEpisode(i));
    li.addEventListener("keydown", (e) => {
      if (e.key === "Enter") goToEpisode(i);
    });
    list.appendChild(li);
  });
}

function goToEpisode(i) {
  if (i < 0 || i >= novel.episodes.length) return;
  currentEpisode = i;
  renderCurrentEpisode(true);
  updateActiveListItem();
  updateProgressBar();
  const sidebar = document.getElementById("sidebar");
  if (sidebar) sidebar.classList.remove("open");
  const panel = document.getElementById("readingPanel");
  if (panel) panel.scrollIntoView({ behavior: "smooth", block: "start" });
}

function renderCurrentEpisode(animate) {
  const titleEl = document.getElementById("episodeTitle");
  const bodyEl = document.getElementById("episodeBody");
  const numberEl = document.getElementById("episodeNumber");
  if (!titleEl || !bodyEl || !numberEl) return;
  const ep = novel.episodes[currentEpisode];

  const renderContent = () => {
    numberEl.textContent = "EPISODE " + String(currentEpisode + 1).padStart(2, "0");
    titleEl.textContent = ep.title;
    bodyEl.innerHTML = ep.paragraphs.map((p) => "<p>" + p + "</p>").join("");
    bodyEl.classList.remove("fade-out");
    bodyEl.classList.remove("fade-in");
    requestAnimationFrame(() => bodyEl.classList.add("fade-in"));
  };

  if (animate) {
    bodyEl.classList.add("fade-out");
    setTimeout(renderContent, 220);
  } else {
    renderContent();
  }

  const prevBtn = document.getElementById("prevBtn");
  const nextBtn = document.getElementById("nextBtn");
  if (prevBtn) prevBtn.disabled = currentEpisode === 0;
  if (nextBtn) {
    nextBtn.textContent =
      currentEpisode === novel.episodes.length - 1 ? "Tamat ✓" : "Episode Selanjutnya →";
    nextBtn.disabled = currentEpisode === novel.episodes.length - 1;
  }
}

function updateActiveListItem() {
  document.querySelectorAll(".episode-item").forEach((li) => {
    li.classList.toggle("active", Number(li.dataset.index) === currentEpisode);
  });
}

function updateProgressBar() {
  const bar = document.getElementById("progressFill");
  if (!bar) return;
  const pct = ((currentEpisode + 1) / novel.episodes.length) * 100;
  bar.style.width = pct + "%";
}

function initReader() {
  const list = document.getElementById("episodeList");
  if (!list) return; // bukan halaman reader

  renderEpisodeList();
  renderCurrentEpisode(false);
  updateActiveListItem();
  updateProgressBar();

  const prevBtn = document.getElementById("prevBtn");
  const nextBtn = document.getElementById("nextBtn");
  if (prevBtn) prevBtn.addEventListener("click", () => goToEpisode(currentEpisode - 1));
  if (nextBtn)
    nextBtn.addEventListener("click", () => {
      if (currentEpisode < novel.episodes.length - 1) goToEpisode(currentEpisode + 1);
    });

  const overlay = document.getElementById("irisOverlay");
  if (overlay) {
    requestAnimationFrame(() => {
      requestAnimationFrame(() => overlay.classList.add("iris-open"));
    });
  }

  const menuToggle = document.getElementById("menuToggle");
  const sidebar = document.getElementById("sidebar");
  if (menuToggle && sidebar) {
    menuToggle.addEventListener("click", () => sidebar.classList.toggle("open"));
  }
}

// ===================================================
// Init
// ===================================================
document.addEventListener("DOMContentLoaded", () => {
  spawnParticles("particles", 30);
  initLanding();
  initReader();
});
